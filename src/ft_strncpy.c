#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*head;

	head = dst;
	while (*src != '\0' && len > 0)
	{
		*dst++ = *src++;
		len--;
	}
	while (len > 0)
	{
		*dst++ = '\0';
		len--;
	}
	return (head);
}

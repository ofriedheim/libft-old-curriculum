#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int i;

	i = -1;
	while (s1[++i] && s2[++i] && n-- > 0)
		if (s1[i] != s2[i])
			return (0);
	return (1);
}
